
package com.blogbasbas.promenengah2.model;

import com.google.gson.annotations.SerializedName;


@SuppressWarnings("unused")
public class DataMakanan {

    @SerializedName("alamat")
    private String mAlamat;
    @SerializedName("foto_makanan")
    private String mFotoMakanan;
    @SerializedName("id_makanan")
    private String mIdMakanan;
    @SerializedName("id_user")
    private String mIdUser;
    @SerializedName("jenkel")
    private String mJenkel;
    @SerializedName("level")
    private String mLevel;
    @SerializedName("makanan")
    private String mMakanan;
    @SerializedName("nama")
    private String mNama;
    @SerializedName("name")
    private String mName;
    @SerializedName("no_telp")
    private String mNoTelp;
    @SerializedName("password")
    private String mPassword;
    @SerializedName("url")
    private String mUrl;
    @SerializedName("username")
    private String mUsername;

    public String getAlamat() {
        return mAlamat;
    }

    public void setAlamat(String alamat) {
        mAlamat = alamat;
    }

    public String getFotoMakanan() {
        return mFotoMakanan;
    }

    public void setFotoMakanan(String fotoMakanan) {
        mFotoMakanan = fotoMakanan;
    }

    public String getIdMakanan() {
        return mIdMakanan;
    }

    public void setIdMakanan(String idMakanan) {
        mIdMakanan = idMakanan;
    }

    public String getIdUser() {
        return mIdUser;
    }

    public void setIdUser(String idUser) {
        mIdUser = idUser;
    }

    public String getJenkel() {
        return mJenkel;
    }

    public void setJenkel(String jenkel) {
        mJenkel = jenkel;
    }

    public String getLevel() {
        return mLevel;
    }

    public void setLevel(String level) {
        mLevel = level;
    }

    public String getMakanan() {
        return mMakanan;
    }

    public void setMakanan(String makanan) {
        mMakanan = makanan;
    }

    public String getNama() {
        return mNama;
    }

    public void setNama(String nama) {
        mNama = nama;
    }

    public String getName() {
        return mName;
    }

    public void setName(String name) {
        mName = name;
    }

    public String getNoTelp() {
        return mNoTelp;
    }

    public void setNoTelp(String noTelp) {
        mNoTelp = noTelp;
    }

    public String getPassword() {
        return mPassword;
    }

    public void setPassword(String password) {
        mPassword = password;
    }

    public String getUrl() {
        return mUrl;
    }

    public void setUrl(String url) {
        mUrl = url;
    }

    public String getUsername() {
        return mUsername;
    }

    public void setUsername(String username) {
        mUsername = username;
    }

}
